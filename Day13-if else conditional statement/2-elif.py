num1 = int(input("Enter your first number: "))
opr = str(input("Enter your operator (+,-,*,/): "))
num2 = int(input("Enter your second number: "))

if(opr == '+'):
    print(num1 + num2)

elif(opr == '-'):
    print(num1 + num2)

elif(opr == '*'):
    print(num1 + num2)

elif(opr == '/'):
    print(num1 + num2)

else:
    print("Invalid Operator")