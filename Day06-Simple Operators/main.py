# varibles
a = 2
b = 3

print("Addition:",a + b)   # 2+3
print("Substracton:",a - b)   # 2-3
print("Multiplication:",a * b)   # 2*3
print("Devision:",a / b)   # 2/3
print("Floor Devision:",a // b)   # 0 (round the nearest value)
print("Exponential:",a ** b)   # 2*2*2
print("Modulus:",a % b)   # 2/3 = 2 (reminder)